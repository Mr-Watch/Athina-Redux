    private void jButtonGrammatiaEgkrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrammatiaEgkrisiActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableGrammatiaEgkriseis.getModel();
        int selectedRow = jTableGrammatiaEgkriseis.getSelectedRow();
        String teacherName = (String) model.getValueAt(selectedRow, 0);
        String studentName = (String) model.getValueAt(selectedRow, 1);
        String studentSubject = (String) model.getValueAt(selectedRow, 2);
        float studentGrade = Float.parseFloat((String) model.getValueAt(selectedRow, 3));
        if (pdfFile == null) {
            jLabelGrammatiaInfo.setText("Δεν υπάρχει αρχέιο υπογραφής");
            return;
        }
        controler.validateTeacherRequest(selectedRow, teacherName, studentName, studentSubject, studentGrade);
        model.removeRow(selectedRow);
        jLabelGrammatiaInfo.setText("Η δήλωση εγκρήθηκε με επιτυχία");
    }//GEN-LAST:event_jButtonGrammatiaEgkrisiActionPerformed

