    private void jButtonTeacherSubmitRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTeacherSubmitRequestActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableTeacherStudentGrades.getModel();
        int row = jTableTeacherStudentGrades.getSelectedRow();
        String studentName = (String) model.getValueAt(row, 0);
        float grade = Float.parseFloat((String) (model.getValueAt(row, 1)));
        String subjectName = jComboBoxTeacherSubjects1.getSelectedItem().toString();
        int result = jFileChooser1.showOpenDialog(this);
        if (result == jFileChooser1.CANCEL_OPTION) {
            //cancel action
        } else if (result == jFileChooser1.APPROVE_OPTION) {
            File selectedFile = jFileChooser1.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            controler.setPdfPath(path);
        }
        controler.makeTeacherRequest(studentName, subjectName, grade);
        model.removeRow(row);
        jLabelTeacherRequestInfo.setText("Η έγκρηση έχει σταλθεί");
    }//GEN-LAST:event_jButtonTeacherSubmitRequestActionPerformed

